#!/usr/bin/env bash
# Module Intelligence System (MIS) — Contract Initializer
# Scans repo and generates initial module contracts (API, DB, Events, Dependencies)
set -eo pipefail

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log_info() { echo -e "${BLUE}[INFO]${NC} $*"; }
log_success() { echo -e "${GREEN}[OK]${NC} $*"; }
log_warn() { echo -e "${YELLOW}[WARN]${NC} $*"; }
log_error() { echo -e "${RED}[ERROR]${NC} $*" >&2; }

REPO_PATH="${1:-.}"
REPO_PATH="$(cd "$REPO_PATH" && pwd)"

if [[ ! -d "$REPO_PATH/.git" ]]; then
  log_error "Not a git repository: $REPO_PATH"
  exit 1
fi

CONTRACT_DIR="$REPO_PATH/.sdlc/module-contracts"
mkdir -p "$CONTRACT_DIR"

TIMESTAMP=$(date -u +"%Y-%m-%dT%H:%M:%SZ")
COMMIT_HASH=$(cd "$REPO_PATH" && git rev-parse HEAD 2>/dev/null || echo "unknown")
MODULE_NAME=$(basename "$REPO_PATH")

log_info "Initializing Module Intelligence System for: $REPO_PATH"
log_info "Module name: $MODULE_NAME"
log_info "Output directory: $CONTRACT_DIR"
echo ""

# ============================================================================
# DETECT STACK
# ============================================================================

detect_stack() {
  local has_pom=0
  local has_gradle=0
  local has_package_json=0
  local has_metro=0
  local has_makefile=0

  [[ -f "$REPO_PATH/pom.xml" ]] && has_pom=1
  [[ -f "$REPO_PATH/build.gradle" || -f "$REPO_PATH/build.gradle.kts" ]] && has_gradle=1
  [[ -f "$REPO_PATH/package.json" ]] && has_package_json=1
  [[ -f "$REPO_PATH/metro.config.js" ]] && has_metro=1
  [[ -f "$REPO_PATH/Makefile" || -f "$REPO_PATH/CMakeLists.txt" ]] && has_makefile=1

  if [[ $has_pom -eq 1 || $has_gradle -eq 1 ]]; then
    echo "java"
  elif [[ $has_package_json -eq 1 && $has_metro -eq 1 ]]; then
    echo "react-native"
  elif [[ $has_package_json -eq 1 ]]; then
    echo "node"
  elif [[ $has_makefile -eq 1 ]]; then
    echo "c"
  else
    echo "unknown"
  fi
}

STACK=$(detect_stack)
log_success "Detected stack: $STACK"
echo ""

# ============================================================================
# EXTRACT API ENDPOINTS (Java only for now)
# ============================================================================

extract_api_endpoints() {
  log_info "Scanning for API endpoints..."
  local endpoints=()

  if [[ "$STACK" == "java" ]]; then
    # Find @RequestMapping, @PostMapping, @GetMapping, @PutMapping, @DeleteMapping
    find "$REPO_PATH/src/main/java" -name "*.java" 2>/dev/null | while read -r file; do
      grep -n "@\(Request\|Get\|Post\|Put\|Delete\|Patch\)Mapping" "$file" 2>/dev/null | head -5
    done | head -20
  fi
}

# ============================================================================
# EXTRACT DATABASE TABLES (from migrations)
# ============================================================================

extract_db_tables() {
  log_info "Scanning for database migrations..."
  local migrations=()

  find "$REPO_PATH" -path "*/db/migrations/*.sql" -o -path "*/src/main/resources/db/migration/*.sql" 2>/dev/null | head -10
}

# ============================================================================
# EXTRACT KAFKA TOPICS
# ============================================================================

extract_kafka_topics() {
  log_info "Scanning for Kafka topics..."

  if [[ "$STACK" == "java" ]]; then
    grep -r "\.topic\|@KafkaListener\|KafkaTemplate\|KafkaProducer" \
      "$REPO_PATH/src/main/java" "$REPO_PATH/src/main/resources" 2>/dev/null | head -20 || true
  fi
}

# ============================================================================
# EXTRACT EXTERNAL DEPENDENCIES
# ============================================================================

extract_dependencies() {
  log_info "Scanning for external dependencies..."

  if [[ -f "$REPO_PATH/pom.xml" ]]; then
    grep -A 2 "<dependency>" "$REPO_PATH/pom.xml" 2>/dev/null | grep -E "<groupId>|<artifactId>" | head -20
  elif [[ -f "$REPO_PATH/build.gradle" || -f "$REPO_PATH/build.gradle.kts" ]]; then
    grep -E "implementation|api|compileOnly" "$REPO_PATH/build.gradle"* 2>/dev/null | head -20
  elif [[ -f "$REPO_PATH/package.json" ]]; then
    cat "$REPO_PATH/package.json" | grep -E '"(dependencies|devDependencies)"' -A 20 | head -30
  fi
}

# ============================================================================
# CREATE API CONTRACT TEMPLATE
# ============================================================================

create_api_contract() {
  log_info "Creating API contract..."
  local contract_file="$CONTRACT_DIR/api-contract.yaml"

  cat > "$contract_file" << 'EOF'
