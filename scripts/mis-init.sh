# endpoints:
#   - path: /users
#     method: POST
#     summary: Create user
#     request_schema: CreateUserRequest
#     response_schema: UserResponse
#     status_codes: [201, 400, 409]
#     breaking_change_notes: null
#     consumers: []
#
#   - path: /users/{id}
#     method: GET
#     summary: Fetch user by ID
#     response_schema: UserResponse
#     status_codes: [200, 404]
#     consumers: []

